public void sendEmail(String recipientEmail, String name, String password, String securityQuestion, String answer, String address) {
    final String senderEmail = "mastimasti227@gmail.com"; // Replace with your email
    final String senderPassword = "ucofqfpgteawhmpk"; // Use the generated App Password

    Properties properties = new Properties();
    properties.put("mail.smtp.auth", "true");
    properties.put("mail.smtp.starttls.enable", "true");
    properties.put("mail.smtp.host", "smtp.gmail.com");
    properties.put("mail.smtp.port", "587");

    // Creating a session with authentication
    Session session = Session.getInstance(properties, new Authenticator() {
        protected PasswordAuthentication getPasswordAuthentication() {
            return new PasswordAuthentication(senderEmail, senderPassword);
        }
    });

    try {
    Message message = new MimeMessage(session);
    message.setFrom(new InternetAddress(senderEmail));
    message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(recipientEmail));
    message.setSubject("Signup Confirmation - Your Account Details");

    // Updated, more attractive email body content
    String emailContent = "Dear " + name + ",\n\n"
            + "Congratulations! Your account has been created successfully in namaste Hotel . We are excited to welcome you!\n\n"
            + "Here are your account details:\n\n"
            + "ðŸ”¹ **Full Name**: " + name + "\n"
            + "ðŸ”¹ **Email**: " + recipientEmail + "\n"
            + "ðŸ”¹ **Security Question**: " + securityQuestion + "\n"
            + "ðŸ”¹ **Answer**: " + answer + "\n"
            + "ðŸ”¹ **Address**: " + address + "\n\n"
            + "For your security, please keep your password confidential and never share it.\n\n"
            + "If you need assistance or have any questions, feel free to reach out to us.\n\n"
            + "<p>Here is the map link to our location: <a href='https://maps.app.goo.gl/khhQUEkNGJ4bnF98A'>Hotel Location</a></p>"
            + "Thank you for choosing us. We're happy to have you on board!\n\n"
            + "Best Regards,\n"
            + "**Your Application Team**";

    message.setText(emailContent);

    // Sending the email
    Transport.send(message);
    JOptionPane.showMessageDialog(null, "Signup Successful! Confirmation Email Sent.");
} catch (MessagingException e) {
    JOptionPane.showMessageDialog(null, "Signup successful, but email could not be sent.\nError: " + e.getMessage());
}

}

    
    
    
    
    
    
    
    
